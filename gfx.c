void putpixel(int x, int y, uint8_t c) {
    if (x >= 0 && x < fb_width && y >= 0 && y < fb_height)
        fb[y * fb_pitch + x] = c;
}

void fill_rect(int x, int y, int w, int h, uint8_t c) {
    for (int dy = 0; dy < h; dy++)
        for (int dx = 0; dx < w; dx++)
            putpixel(x + dx, y + dy, c);
}

void hline(int x, int y, int w, uint8_t c) {
    for (int i = 0; i < w; i++) putpixel(x + i, y, c);
}

void vline(int x, int y, int h, uint8_t c) {
    for (int i = 0; i < h; i++) putpixel(x, y + i, c);
}

void draw_raised_box(int x, int y, int w, int h) {
    hline(x, y, w, BUTTON_HILIGHT);
    vline(x, y, h, BUTTON_HILIGHT);
    hline(x, y + h - 1, w, BUTTON_SHADOW);
    vline(x + w - 1, y, h, BUTTON_SHADOW);
}

void draw_sunken_box(int x, int y, int w, int h) {
    hline(x, y, w, BUTTON_SHADOW);
    vline(x, y, h, BUTTON_SHADOW);
    hline(x, y + h - 1, w, BUTTON_HILIGHT);
    vline(x + w - 1, y, h, BUTTON_HILIGHT);
}

